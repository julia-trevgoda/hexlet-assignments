package exercise;

public class User {

     String firstName;
     String lastName;
     String id;
     String email;

    public User() {}

    public String getId() {
        return id;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getEmail() {
        return email;
    }
}
